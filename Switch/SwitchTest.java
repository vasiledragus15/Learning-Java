package Switch;

public class SwitchTest {
    public static void main(String[] args) {
        for (int i = 0; i < 6; i++) {
            switch (i) {
                case 0:
                    System.out.println("Suca");
                    break;
                case 1:
                    System.out.println("Coglione");
                    break;
                case 2:
                    System.out.println("Pezzo");
                    break;
                case 3:
                    System.out.println("Di");
                    break;
                case 4:
                    System.out.println("Merda");
                    break;
                default:
                System.out.println("Haha non funziona un cazzo");
            
            }
        }
    }
}
